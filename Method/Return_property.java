package Method;

public class Return_property {
	
	public static int one() {
		
		return 1; //return 반환 하는 순간 function 또는 method에서 나가게 된다.
		//return 2;
		//return 3;
	}

	public static void main(String[] args) {
		//처음에 있는 String[] args는 처음 시작할때 주는 배열의 값이 된다.
		System.out.println(args.length);
		//String[] args - String 배열로 
		//자바가 main을 실행시켜준다.
	}

}
